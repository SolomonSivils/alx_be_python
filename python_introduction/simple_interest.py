# calculate simple interest earned.
# define principal whcih is initial investment
# P = $1000
principal = 1000

# define annual interest as decimal
# R = 5% = 0.05
rate = 0.05

# define number of years
# T = 3 years
time = 3

# calculate simple interest using formula: (I = P * R * T)
# I = interest earned
interest = principal * rate * time
print(f"The simple interest is: {interest}")
