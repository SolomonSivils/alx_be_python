# convert hours into seconds

# define hours to convert
# H = 2 hours
hours = 2

# calculate number of seconds in the given hours (3600 seconds in an hour)
# 60 minutes in an hour, 60 seconds in a minute, therefore (60 * 60 = 3600)
# seconds_in_an_hour = 3600

# convert 2 hours
seconds = hours * 3600
print(f"{hours} hour(s) is {seconds} seconds")